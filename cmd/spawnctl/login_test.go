func seedLoginTestFamilyWithSPKI(t *testing.T, st store.Store, accountID string, spkiDER []byte, now time.Time) string {
	t.Helper()
	b := make([]byte, 32)
	if _, err := rand.Read(b); err != nil {
		t.Fatal(err)
	}
	rawToken := base64.RawURLEncoding.EncodeToString(b)
	sum := sha256.Sum256([]byte(rawToken))
	tokenHash := fmt.Sprintf("%x", sum[:])

	row := store.RefreshSession{
		TokenHash:         tokenHash,
		AccountID:         accountID,
		FamilyID:          "fam-" + accountID,
		ClientKind:        store.ClientCLI,
		SessionPubkeySPKI: spkiDER,
		AccessTokenID:     "tok-" + accountID,
		CreatedAt:         now.Unix(),
		LastUsedAt:        now.Unix(),
		ExpiresAt:         now.Add(30 * 24 * time.Hour).Unix(),
		FamilyCreatedAt:   now.Unix(),
	}
	if err := st.RefreshSessions().Insert(context.Background(), row); err != nil {
		t.Fatal(err)
	}
	return rawToken
}

func genLoginTestP256(t *testing.T) (*ecdsa.PrivateKey, []byte) {
	t.Helper()
	k, err := ecdsa.GenerateKey(elliptic.P256(), rand.Reader)
	if err != nil {
		t.Fatal(err)
	}
	der, err := x509.MarshalPKIXPublicKey(&k.PublicKey)
	if err != nil {
		t.Fatal(err)
	}
	return k, der
}

