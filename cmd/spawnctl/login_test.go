// lazyTestMux allows wiring the AS handler after the server URL is known.
type lazyTestMux struct {
	real http.Handler
}

func (l *lazyTestMux) ServeHTTP(w http.ResponseWriter, r *http.Request) {
	if l.real == nil {
		http.Error(w, "not ready", http.StatusServiceUnavailable)
		return
	}
	l.real.ServeHTTP(w, r)
}

// buildTestAS creates a real authsvc Service + httptest.Server with a loopback anchor registered.
// nowFn is the clock the AS uses; pass nil to use real time (required for /refresh PoP tests).
func buildTestAS(t *testing.T, fake *githubfake.Fake, nowFn func() time.Time) (*httptest.Server, store.Store, authsvc.IdPConfig) {
	t.Helper()

	_, sigKey, _ := ed25519.GenerateKey(rand.Reader)
	st := store.NewTestStore(t)

	root, err := pki.NewRootCA("Test Root")
	if err != nil {
		t.Fatal(err)
	}
	inter, err := root.NewIntermediate(pki.ClassSelfHosted)
	if err != nil {
		t.Fatal(err)
	}

	lm := &lazyTestMux{}
	srv := httptest.NewServer(lm)
	t.Cleanup(srv.Close)

	if nowFn == nil {
		nowFn = time.Now
	}
	cfg := authsvc.IdPConfig{
		Store:               st,
		GitHub:              authsvc.NewGitHubProvider(fake.URL(), fake.URL(), fake.ClientID, fake.ClientSecret),
		SigningKey:           sigKey,
		GitHubRedirectURI:   srv.URL + "/oauth/callback",
		SPAOrigin:           "http://localhost:3000",
		RedirectURIs:        []string{"http://localhost:3000/callback", "http://127.0.0.1:8000/cb"},
		VerificationURI:     srv.URL + "/device/verify",
		RegistrationEnabled: true,
		Now:                 nowFn,
	}
	idp, err := authsvc.NewIdP(cfg)
	if err != nil {
		t.Fatal(err)
	}
	svc := authsvc.New(root.Cert, inter, authsvc.WithIdP(idp))
	lm.real = svc.Handler()

	return srv, st, cfg
}

// noRedirClient returns an http.Client that does not follow redirects.
func noRedirClient() *http.Client {
	return &http.Client{
		CheckRedirect: func(*http.Request, []*http.Request) error {
			return http.ErrUseLastResponse
		},
	}
}

// driveLoopbackFlow acts as the "browser": authorize → githubfake → AS callback → spawnctl /cb.
// sessKeyB64 is the session pubkey embedded in the authorize URL; cbURL is the spawnctl listener.
// csrfState must match the state generated by loginLoopback (extracted from the authorize URL it prints).
func driveLoopbackFlow(t *testing.T, asSrv *httptest.Server, fake *githubfake.Fake, cbURL, sessKeyB64, csrfState string) {
	t.Helper()
	cl := noRedirClient()

	authURL := asSrv.URL + "/oauth/authorize?" + url.Values{
		"redirect_uri":          {cbURL},
		"state":                 {csrfState},
		"session_pubkey":        {sessKeyB64},
		"code_challenge":        {"E9Melhoa2OwvFrEMTJguCHaoeK1t8URWbuGJSstw-cM"},
		"code_challenge_method": {"S256"},
	}.Encode()

	authResp, err := cl.Get(authURL)
	if err != nil {
		t.Fatalf("driveLoopback/authorize: %v", err)
	}
	if authResp.StatusCode != http.StatusFound {
		body, _ := io.ReadAll(authResp.Body)
		t.Fatalf("driveLoopback/authorize: want 302, got %d: %s", authResp.StatusCode, body)
	}
	flowCookie := ""
	for _, c := range authResp.Cookies() {
		if c.Name == "as_flow" {
			flowCookie = c.Value
		}
	}
	if flowCookie == "" {
		t.Fatal("driveLoopback: no as_flow cookie")
	}
	ghURL := authResp.Header.Get("Location")

	ghResp, err := cl.Get(ghURL)
	if err != nil {
		t.Fatalf("driveLoopback/githubfake: %v", err)
	}
	callbackURL := ghResp.Header.Get("Location")
	if callbackURL == "" {
		t.Fatalf("driveLoopback/githubfake: no Location, status=%d", ghResp.StatusCode)
	}

	cbReq, _ := http.NewRequest("GET", callbackURL, nil)
	cbReq.AddCookie(&http.Cookie{Name: "as_flow", Value: flowCookie})
	cbResp, err := cl.Do(cbReq)
	if err != nil {
		t.Fatalf("driveLoopback/callback: %v", err)
	}
	if cbResp.StatusCode != http.StatusFound {
		body, _ := io.ReadAll(cbResp.Body)
		t.Fatalf("driveLoopback/callback: want 302, got %d: %s", cbResp.StatusCode, body)
	}
	// Follow final redirect to spawnctl's /cb listener.
	finalURL := cbResp.Header.Get("Location")
	finalResp, err := http.Get(finalURL)
	if err != nil {
		t.Fatalf("driveLoopback/cb hit: %v", err)
	}
	finalResp.Body.Close()
}

// TestLoopbackLoginEndToEnd: loopback PKCE flow writes auth.json with access + refresh + key.
// Uses --no-browser so loginLoopback prints the URL; the test parses it to drive the flow.
func TestLoopbackLoginEndToEnd(t *testing.T) {
	fake := githubfake.New()
	defer fake.Close()
	fake.SetUser(10002, "looptest2")
	now := time.Unix(1770000000, 0)
	srv, _, _ := buildTestAS(t, fake, func() time.Time { return now })

	dir := t.TempDir()

	// loginLoopback writes the authorize URL to pw; test reads from pr.
	pr, pw := io.Pipe()

	loginErrCh := make(chan error, 1)
	go func() {
		err := loginLoopback(context.Background(), dir, srv.URL, true, pw)
		pw.CloseWithError(err) // propagate error through pipe
		loginErrCh <- err
	}()

	// Read lines until we find the authorize URL.
	scanner := bufio.NewScanner(pr)
	var authURL string
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if strings.HasPrefix(line, srv.URL+"/oauth/authorize") {
			authURL = line
			break
		}
	}
	if authURL == "" {
		t.Fatal("could not find authorize URL in loginLoopback output")
	}

	// Extract cbURL and session pubkey from the authorize URL.
	parsedAuth, err := url.Parse(authURL)
	if err != nil {
		t.Fatalf("parse authorize URL: %v", err)
	}
	cbURL := parsedAuth.Query().Get("redirect_uri")
	sessKeyB64 := parsedAuth.Query().Get("session_pubkey")
	csrfState := parsedAuth.Query().Get("state")
	if cbURL == "" || sessKeyB64 == "" || csrfState == "" {
		t.Fatalf("missing redirect_uri, session_pubkey, or state in authorize URL: %s", authURL)
	}

	// Drive the browser flow using the CSRF state generated by loginLoopback.
	driveLoopbackFlow(t, srv, fake, cbURL, sessKeyB64, csrfState)

	// Drain remaining output so loginLoopback can finish writing.
	go io.Copy(io.Discard, pr)

	if err := <-loginErrCh; err != nil {
		t.Fatalf("loginLoopback: %v", err)
	}

	// Verify auth.json.
	info, err := os.Stat(filepath.Join(dir, authStateFile))
	if err != nil {
		t.Fatalf("auth.json missing: %v", err)
	}
	if info.Mode().Perm() != 0o600 {
		t.Fatalf("auth.json perms: want 0600, got %o", info.Mode().Perm())
	}
	s, err := loadState(dir)
	if err != nil || s == nil {
		t.Fatalf("loadState: %v", err)
	}
	if s.AccessToken == "" {
		t.Fatal("access_token empty")
	}
	if s.RefreshToken == "" {
		t.Fatal("refresh_token empty — loopback refresh_token seam not working")
	}
	if s.SessionKeyPKCS8PEM == "" {
		t.Fatal("session_key_pkcs8_pem empty")
	}
	// Bearer header check: token source returns the stored access token.
	src := buildTokenSource(dir, "dev-token", &http.Client{})
	tok, err := src.Token(context.Background())
	if err != nil {
		t.Fatalf("Token: %v", err)
	}
	if tok != s.AccessToken {
		t.Fatalf("token source returned %q; want stored access token %q", tok, s.AccessToken)
	}
}

// TestDeviceFlow: device grant persists tokens on approval.
// Note: the poll interval from the real AS is 5s; test may take ~5s.
func TestDeviceFlow(t *testing.T) {
	if testing.Short() {
		t.Skip("skipping device flow test in short mode (polls 5s)")
	}

	fake := githubfake.New()
	defer fake.Close()
	fake.SetUser(10003, "devtest")
	now := time.Unix(1770000000, 0)
	srv, st, _ := buildTestAS(t, fake, func() time.Time { return now })

	dir := t.TempDir()

	// Capture loginDevice's output through a pipe to extract the user_code.
	pr, pw := io.Pipe()
	loginErrCh := make(chan error, 1)
	go func() {
		err := loginDevice(context.Background(), dir, srv.URL, pw)
		pw.CloseWithError(err)
		loginErrCh <- err
	}()

	// Read lines until we find the user_code line.
	scanner := bufio.NewScanner(pr)
	var userCode string
	for scanner.Scan() {
		line := scanner.Text()
		if strings.Contains(line, "And enter code:") {
			parts := strings.Fields(line)
			if len(parts) > 0 {
				userCode = parts[len(parts)-1]
			}
			break
		}
	}
	if userCode == "" {
		t.Fatal("could not capture user_code from loginDevice output")
	}

	// Seed a browser session and approve the grant.
	seedLoginTestUser(t, st, "acct-dev", 10003, now)
	browserToken := seedLoginTestFamily(t, st, "acct-dev", now)

	verifyReq, _ := http.NewRequest("POST", srv.URL+"/device/verify",
		strings.NewReader(url.Values{"user_code": {userCode}}.Encode()))
	verifyReq.Header.Set("Content-Type", "application/x-www-form-urlencoded")
	verifyReq.AddCookie(&http.Cookie{Name: "device_session", Value: browserToken, Path: "/device"})
	resp, err := http.DefaultClient.Do(verifyReq)
	if err != nil {
		t.Fatalf("device/verify: %v", err)
	}
	resp.Body.Close()

	// Drain remaining output and wait for loginDevice to finish.
	go io.Copy(io.Discard, pr)

	if err := <-loginErrCh; err != nil {
		t.Fatalf("loginDevice: %v", err)
	}

	s, err := loadState(dir)
	if err != nil || s == nil {
		t.Fatalf("loadState: %v", err)
	}
	if s.AccessToken == "" {
		t.Fatal("access_token empty after device flow")
	}
	if s.RefreshToken == "" {
		t.Fatal("refresh_token empty after device flow")
	}
	if s.SessionKeyPKCS8PEM == "" {
		t.Fatal("session key empty after device flow")
	}
}

// TestConcurrentRefreshSingleFlight: N goroutines call Token() on a near-expiry state;
// exactly one /refresh must reach the AS; all goroutines get the same fresh token.
func TestConcurrentRefreshSingleFlight(t *testing.T) {
	fake := githubfake.New()
	defer fake.Close()
	fake.SetUser(10004, "refreshtest")
	// Use nil (real time) for the AS clock — PoP signatures use time.Now() and must not skew.
	// Seeded sessions must also use time.Now() so they are not already expired at real time.
	srv, st, _ := buildTestAS(t, fake, nil)

	dir := t.TempDir()

	// Generate a session key and get a real refresh family via device grant.
	sessKey, err := generateSessionKey()
	if err != nil {
		t.Fatal(err)
	}
	pubB64, err := sessionPubkeySPKIB64(sessKey)
	if err != nil {
		t.Fatal(err)
	}
	spkiDER, _ := x509.MarshalPKIXPublicKey(&sessKey.PublicKey)
	keyPEM, err := marshalSessionKey(sessKey)
	if err != nil {
		t.Fatal(err)
	}

	// Seed user + device grant + approve it.
	seedLoginTestUser(t, st, "acct-refresh", 10004, time.Now())

	form := url.Values{"session_pubkey": {pubB64}, "client_kind": {"cli"}}
	authResp, err := http.PostForm(srv.URL+"/device/authorize", form)
	if err != nil {
		t.Fatal(err)
	}
	var authOut struct {
		DeviceCode string `json:"device_code"`
		UserCode   string `json:"user_code"`
	}
	body, _ := io.ReadAll(authResp.Body)
	authResp.Body.Close()
	if err := json.Unmarshal(body, &authOut); err != nil || authOut.DeviceCode == "" {
		t.Fatalf("device/authorize: %v: %s", err, body)
	}

	// Seed with real time so the session is not already expired when the AS (using real time) checks it.
	browserToken := seedLoginTestFamilyWithSPKI(t, st, "acct-refresh", spkiDER, time.Now())
	verifyReq, _ := http.NewRequest("POST", srv.URL+"/device/verify",
		strings.NewReader(url.Values{"user_code": {authOut.UserCode}}.Encode()))
	verifyReq.Header.Set("Content-Type", "application/x-www-form-urlencoded")
	verifyReq.AddCookie(&http.Cookie{Name: "device_session", Value: browserToken, Path: "/device"})
	vr, _ := http.DefaultClient.Do(verifyReq)
	if vr != nil {
		vrBody, _ := io.ReadAll(vr.Body)
		vr.Body.Close()
		if vr.StatusCode != http.StatusOK {
			t.Fatalf("device/verify: status=%d body=%s", vr.StatusCode, vrBody)
		}
	}

	// Poll for tokens.
	var tokenOut struct {
		AccessToken  string `json:"access_token"`
		RefreshToken string `json:"refresh_token"`
	}
	for i := 0; i < 20; i++ {
		time.Sleep(100 * time.Millisecond)
		tr, err := http.PostForm(srv.URL+"/device/token",
			url.Values{"device_code": {authOut.DeviceCode}})
		if err != nil {
			continue
		}
		b2, _ := io.ReadAll(tr.Body)
		tr.Body.Close()
		if json.Unmarshal(b2, &tokenOut) == nil && tokenOut.AccessToken != "" {
			break
		}
	}
	if tokenOut.AccessToken == "" {
		t.Fatal("could not get tokens via device flow for refresh test setup")
	}

	// Seed a near-expiry authState.
	s := &authState{
		ASURL:              srv.URL,
		AccessToken:        tokenOut.AccessToken,
		AccessExpiresAt:    time.Now().Add(5 * time.Second).Unix(), // within refreshWindow (2min)
		RefreshToken:       tokenOut.RefreshToken,
		SessionKeyPKCS8PEM: keyPEM,
	}
	if err := saveState(dir, s); err != nil {
		t.Fatal(err)
	}

	// Wrap the server's handler with a /refresh counter.
	origHandler := srv.Config.Handler
	var refreshCount int32
	srv.Config.Handler = http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if r.Method == http.MethodPost && r.URL.Path == "/refresh" {
			atomic.AddInt32(&refreshCount, 1)
		}
		origHandler.ServeHTTP(w, r)
	})

	// Launch N goroutines calling Token() concurrently.
	const N = 10
	src := &cpTokenSource{dir: dir, httpClient: &http.Client{}}
	results := make([]string, N)
	errs := make([]error, N)
	var wg sync.WaitGroup
	for i := 0; i < N; i++ {
		i := i
		wg.Add(1)
		go func() {
			defer wg.Done()
			results[i], errs[i] = src.Token(context.Background())
		}()
	}
	wg.Wait()

	// Restore original handler.
	srv.Config.Handler = origHandler

	// Assert exactly one /refresh hit.
	if got := atomic.LoadInt32(&refreshCount); got != 1 {
		t.Fatalf("want exactly 1 /refresh, got %d", got)
	}

	// Assert all goroutines got the same non-empty token.
	first := results[0]
	if first == "" {
		t.Fatal("results[0] empty")
	}
	for i, tok := range results {
		if errs[i] != nil {
			t.Errorf("goroutine %d error: %v", i, errs[i])
		}
		if tok != first {
			t.Errorf("goroutine %d token %q != %q", i, tok, first)
		}
	}
}

