// TestPoPRoundTrip: signRefreshPoP → authsvc.VerifyPoP cross-validates the frozen byte layout.
func TestPoPRoundTrip(t *testing.T) {
	sessKey, err := generateSessionKey()
	if err != nil {
		t.Fatal(err)
	}
	rawRefresh := "test-refresh-token-abc"

	tsStr, nonceB64, sigB64, err := signRefreshPoP(rawRefresh, sessKey)
	if err != nil {
		t.Fatalf("signRefreshPoP: %v", err)
	}

	var ts int64
	if _, err := fmt.Sscanf(tsStr, "%d", &ts); err != nil {
		t.Fatal(err)
	}
	nonce, _ := base64.RawURLEncoding.DecodeString(nonceB64)
	sig, _ := base64.RawURLEncoding.DecodeString(sigB64)
	spkiDER, _ := x509.MarshalPKIXPublicKey(&sessKey.PublicKey)

	tokenHash := sha256.Sum256([]byte(rawRefresh))
	proof := authsvc.PoPProof{
		RefreshTokenHash: tokenHash[:],
		Timestamp:        ts,
		Nonce:            nonce,
		Sig:              sig,
	}
	if err := authsvc.VerifyPoP(spkiDER, proof, time.Unix(ts, 0)); err != nil {
		t.Fatalf("VerifyPoP: %v", err)
	}
}

