// Codegen: read every Windows-Terminal scheme JSON under scripts/schemes/, map each to an xterm
// ITheme via the pure wtToITheme(), and emit a committed src/term/themes.gen.ts.
//
// Run: `node scripts/gen-term-themes.mjs` (npm: `gen:themes`). Deterministic — running twice
// produces no diff (schemes + keys are sorted).
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { wtToITheme } from "./wt.mjs";

const here = dirname(fileURLToPath(import.meta.url));
const schemesDir = join(here, "schemes");
const outFile = join(here, "..", "src", "term", "themes.gen.ts");

const files = readdirSync(schemesDir)
  .filter((f) => f.endsWith(".json"))
  .sort((a, b) => a.localeCompare(b, "en"));

if (files.length === 0) {
  throw new Error(`No scheme JSON found in ${schemesDir}`);
}

/** @type {Record<string, Record<string, string>>} */
const themes = {};
for (const file of files) {
  const raw = JSON.parse(readFileSync(join(schemesDir, file), "utf8"));
  // Display name: prefer the scheme's own `name`, fall back to the file stem.
  const name = typeof raw.name === "string" && raw.name.trim() ? raw.name : file.replace(/\.json$/, "");
  if (themes[name]) {
    throw new Error(`Duplicate scheme display name "${name}" (from ${file})`);
  }
  themes[name] = wtToITheme(raw); // throws on any missing/invalid field
}

const names = Object.keys(themes).sort((a, b) => a.localeCompare(b, "en"));

// ITheme key order in each emitted object — stable & readable.
const KEY_ORDER = [
  "background",
  "foreground",
  "cursor",
  "cursorAccent",
  "selectionBackground",
  "selectionInactiveBackground",
  "black",
  "red",
  "green",
  "yellow",
  "blue",
  "magenta",
  "cyan",
  "white",
  "brightBlack",
  "brightRed",
  "brightGreen",
  "brightYellow",
  "brightBlue",
  "brightMagenta",
  "brightCyan",
  "brightWhite",
];

/** @param {string} s */
const q = (s) => JSON.stringify(s);

function emitTheme(theme) {
  const lines = KEY_ORDER.map((k) => `    ${k}: ${q(theme[k])},`);
  return `{\n${lines.join("\n")}\n  }`;
}

const entries = names.map((n) => `  ${q(n)}: ${emitTheme(themes[n])},`).join("\n");
const nameList = names.map((n) => `  ${q(n)},`).join("\n");

const out = `// GENERATED by scripts/gen-term-themes.mjs — do not edit by hand.
// Source: mbadolato/iTerm2-Color-Schemes (Windows-Terminal variant). Scheme names preserved
// for attribution. Regenerate with \`npm run gen:themes\`.
import type { ITheme } from "@xterm/xterm";

export const TERM_THEMES: Record<string, ITheme> = {
${entries}
};

export const TERM_THEME_NAMES: string[] = [
${nameList}
];
`;

writeFileSync(outFile, out, "utf8");
console.log(`Wrote ${outFile} (${names.length} themes)`);
